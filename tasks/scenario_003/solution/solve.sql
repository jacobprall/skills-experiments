USE ROLE {admin_role};
USE WAREHOUSE {warehouse};

CALL SYSTEM$CLASSIFY('{database}.{raw_schema}.CUSTOMERS', {'auto_tag': true});

CREATE OR REPLACE MASKING POLICY {database}.{governance_schema}.MASK_SSN AS
(val STRING) RETURNS STRING -> CASE WHEN CURRENT_ROLE() IN ('{admin_role}') THEN val ELSE '***-**-****' END;
CREATE OR REPLACE MASKING POLICY {database}.{governance_schema}.MASK_EMAIL AS
(val STRING) RETURNS STRING -> CASE WHEN CURRENT_ROLE() IN ('{admin_role}') THEN val ELSE '***@' || SPLIT_PART(val, '@', 2) END;
CREATE OR REPLACE MASKING POLICY {database}.{governance_schema}.MASK_PHONE AS
(val STRING) RETURNS STRING -> CASE WHEN CURRENT_ROLE() IN ('{admin_role}') THEN val ELSE '***-***-' || RIGHT(val, 4) END;
CREATE OR REPLACE MASKING POLICY {database}.{governance_schema}.MASK_DOB AS
(val DATE) RETURNS DATE -> CASE WHEN CURRENT_ROLE() IN ('{admin_role}') THEN val ELSE '1900-01-01'::DATE END;
CREATE OR REPLACE MASKING POLICY {database}.{governance_schema}.MASK_NAME AS
(val STRING) RETURNS STRING -> CASE WHEN CURRENT_ROLE() IN ('{admin_role}') THEN val ELSE 'REDACTED' END;

ALTER TABLE {database}.{raw_schema}.CUSTOMERS MODIFY COLUMN SSN SET MASKING POLICY {database}.{governance_schema}.MASK_SSN;
ALTER TABLE {database}.{raw_schema}.CUSTOMERS MODIFY COLUMN EMAIL SET MASKING POLICY {database}.{governance_schema}.MASK_EMAIL;
ALTER TABLE {database}.{raw_schema}.CUSTOMERS MODIFY COLUMN PHONE SET MASKING POLICY {database}.{governance_schema}.MASK_PHONE;
ALTER TABLE {database}.{raw_schema}.CUSTOMERS MODIFY COLUMN DATE_OF_BIRTH SET MASKING POLICY {database}.{governance_schema}.MASK_DOB;
ALTER TABLE {database}.{raw_schema}.CUSTOMERS MODIFY COLUMN CUSTOMER_NAME SET MASKING POLICY {database}.{governance_schema}.MASK_NAME;

CREATE OR REPLACE VIEW {database}.{analytics_schema}.DATA_QUALITY_REPORT AS
SELECT 'CUSTOMERS' AS table_name, 'null_ssn' AS check_name, COUNT(*) AS issues FROM {database}.{raw_schema}.CUSTOMERS WHERE SSN IS NULL
UNION ALL SELECT 'ORDERS', 'null_order_id', COUNT(*) FROM {database}.{raw_schema}.ORDERS WHERE ORDER_ID IS NULL
UNION ALL SELECT 'ORDERS', 'negative_amount', COUNT(*) FROM {database}.{raw_schema}.ORDERS WHERE TOTAL_AMOUNT < 0
UNION ALL SELECT 'SUPPORT_TICKETS', 'null_customer', COUNT(*) FROM {database}.{raw_schema}.SUPPORT_TICKETS WHERE CUSTOMER_ID IS NULL;

CREATE OR REPLACE VIEW {database}.{analytics_schema}.GOVERNANCE_STATUS AS
SELECT
    (SELECT COUNT(*) FROM {database}.INFORMATION_SCHEMA.TABLES WHERE TABLE_SCHEMA = '{raw_schema}') AS TOTAL_TABLES,
    (SELECT COUNT(DISTINCT REF_ENTITY_NAME) FROM TABLE(INFORMATION_SCHEMA.POLICY_REFERENCES(REF_ENTITY_NAME => '{database}.{raw_schema}.CUSTOMERS', REF_ENTITY_DOMAIN => 'TABLE')) WHERE POLICY_KIND = 'MASKING_POLICY') AS TABLES_WITH_MASKING,
    (SELECT COUNT(*) FROM TABLE(INFORMATION_SCHEMA.TAG_REFERENCES('{database}.{raw_schema}.CUSTOMERS', 'TABLE')) WHERE TAG_NAME = 'SEMANTIC_CATEGORY') AS PII_TAGGED_COLUMNS;
